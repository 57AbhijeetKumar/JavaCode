public class trapeziumPattern {
        public static void main (String[] args) throws java.lang.Exception
        {
            // your code goes here
            int num = 4;
            for (int i=1;i<=num;i++){
                for(int j=1;j<=4;j++){
                    System.out.print(j+"*");
                }
                System.out.println();
            }
        }
    }

